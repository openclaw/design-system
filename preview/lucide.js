// Guard-test surface: markup may only reference names present in this curated
// subset; unregistered names render as nothing at runtime.
export const registeredIconNames = Object.freeze(Object.keys(icons));

export function createIcons(options = {}) {
  const {
    root = document,
    nameAttr = "data-lucide",
    attrs = {},
    inTemplates = false,
  } = options;

  for (const element of root.querySelectorAll(`[${nameAttr}]`)) {
    const name = element.getAttribute(nameAttr);
    const icon = icons[name];
    if (!icon) {
      console.warn(`Lucide icon "${name}" is not registered.`);
      continue;
    }

    const elementAttrs = Object.fromEntries(
      [...element.attributes].map(({ name: attributeName, value }) => [attributeName, value]),
    );
    const className = ["lucide", `lucide-${name}`, elementAttrs.class, attrs.class]
      .filter(Boolean)
      .join(" ");
    const svg = createElement(icon, {
      "aria-hidden": elementAttrs["aria-hidden"] ?? "true",
      ...attrs,
      ...elementAttrs,
      class: className,
    });
    element.replaceWith(svg);
  }

  if (inTemplates) {
    for (const template of root.querySelectorAll("template")) {
      createIcons({ ...options, root: template.content });
    }
  }
}

export const lucide = { createIcons };
